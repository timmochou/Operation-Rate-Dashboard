SELECT 
ID,
DEPT,
LINE,
STATIONID,
EQIPID,
STIME,
ETIME,
MSTATUS,
OPSTATUS,
Duration,
Memo,
MODU,
MDATE,
CREATEAT,
isDel,
REMARK1,
EQUIPNAME,
EQUIP
FROM [dbo].[TMESEQPRUNTIME] T1 
LEFT JOIN [dbo].[TMIMDCDE] T2 
ON T1.[OPSTATUS] = REVERSE(T2.[CODE])
LEFT JOIN [dbo].[TMIMEQUIP] T3
ON T1.[EQPID] = T3.[ASSETNO]